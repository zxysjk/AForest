package com.example.aforest.contact;

public class contactItem2 {
	private String name;
	private String sex;
	private String phone;


	contactItem2(String name, String sex, String phone) {
		this.name = name;
		this.sex = sex;
		this.phone = phone;

	}

	public String getName() {
		return this.name;
	}

	public String getSex() {
		return this.sex;
	}

	public String getPhone() {
		return this.phone;
	}


}
